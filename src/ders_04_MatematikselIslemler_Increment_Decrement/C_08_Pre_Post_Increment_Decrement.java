package ders_04_MatematikselIslemler_Increment_Decrement;

public class C_08_Pre_Post_Increment_Decrement {
    public static void main(String[] args) {

        int x = 3 ;        // x=3
        int y = 2 * ++x ;  // y=8, x=4
        int z = 5 + y-- ;  // z=13, y=7, x=4 >> 24

        System.out.println(x+y+z);












    }
}
