package ders_13_NestedForLoop_MethodOlusturma;

public class C_04_NestedForLoop {
    public static void main(String[] args) {

        /*
        Asagidaki sekli yazdiran bir kod hazirlayin

        * * * * * *
        * * * * * *
        * * * * * *
        * * * * * *

         */

        for (int i = 1; i <=4 ; i++) {   // satirlari kontrol ediyor

            for (int j = 1; j <=6 ; j++) {  // sutunlari kontrol ediyor

                System.out.print("* ");

            }
            System.out.println(""); // Hala ilk for'un icinde..ic loop bitince alt satira gec demek..
        }










    }
}
