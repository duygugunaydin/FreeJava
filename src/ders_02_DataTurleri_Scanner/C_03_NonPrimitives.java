        System.out.println(isim.toLowerCase());  //ali akpinar

        //isim yazip noktaya basinca bir suru hazr methodlar cikar.
        //non-primitive data turleri deger yaninda hazir methodlar da bulundururlar.


        //NAMING CONVENTION//
        //bankadaki hesaplari tuttugum bir uygulama

        int birinciHesap = 100 ;
