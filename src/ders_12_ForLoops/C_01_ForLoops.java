package ders_12_ForLoops;

public class C_01_ForLoops {
    public static void main(String[] args) {

        String str = "Java\n";    // >> alt satira inmesi icin sonuna \n ekledik..

        System.out.println(str.repeat(10));

        // Repeat olmasaydi >> tek tek yazmak zorunda kalacaktik..

        System.out.println(str);
        System.out.println(str);
        System.out.println(str);
        System.out.println(str);
        System.out.println(str);  // >> gibi..



















    }
}
